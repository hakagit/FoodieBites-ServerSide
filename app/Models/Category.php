 * @method static \Illuminate\Database\Eloquent\Builder|Category withoutTrashed()
 * @mixin \Eloquent
 */
class Category extends Model
{
    use HasFactory, SoftDeletes;

    protected $table = 'categories'; // Specify the table name if necessary

    // Specify which attributes are mass assignable
    protected $fillable = [
        'name',
        'quantity',
        'user_id', // Foreign key to the Admin model
        'image',

    ];

    // Define the relationship with Admin
    public function user()
    {
        return $this->belongsTo(User::class);
    }
    // Define the relationship with Dish
    public function dishes()
    {
        return $this->hasMany(Dish::class); // One category can have many dishes
    }
}
